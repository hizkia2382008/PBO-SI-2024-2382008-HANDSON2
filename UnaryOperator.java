public class UnaryOperator {
    public static void main(String[] args) {
        int x = 10;
        // pre-increment
        System.out.println(++x);

        //Post Increment
        System.out.println(x++);

        int y = 10;
        //Pre-Increment
        System.out.println(--y);

        //Post increment
        System.out.println(y--);

        boolean isTrue = true;
        System.out.println(!isTrue);
    }
}
